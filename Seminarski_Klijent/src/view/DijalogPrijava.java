    private void btnAzurirajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAzurirajActionPerformed
        ep.setKlub((PlivackiKlub) comboKlub.getSelectedItem());
        ep.setTak((Takmicenje)comboTakmicenje.getSelectedItem());
        Kontroler.getInstance().azurirajPrijavu(ep);
        JOptionPane.showMessageDialog(this, "Uspesno ste ažurirali prijavu", "Ažuriranje uspesno", JOptionPane.INFORMATION_MESSAGE);
        this.dispose();
        
    }//GEN-LAST:event_btnAzurirajActionPerformed

    private void popuniComboKlubovi() {
        List<PlivackiKlub> pks = kontroler.vratiKlubove();
        for (PlivackiKlub pk : pks) {
            comboKlub.addItem(pk);
        }
    }

    private void popuniComboTakmicenja() {
        List<Takmicenje> taks = kontroler.vratiTakmicenja();
        for (Takmicenje t : taks) {
            comboTakmicenje.addItem(t);
        }
    }

