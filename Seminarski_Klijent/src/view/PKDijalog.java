    private void btnAzurirajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAzurirajActionPerformed
        PlivackiKlub p = new PlivackiKlub(Long.parseLong(tfID.getText()), tfNaziv.getText(),
                Integer.parseInt(tfBrPliv.getText()), Integer.parseInt(tfBrTren.getText()),
                (Mesto) comboMesto.getSelectedItem());
        Kontroler.getInstance().izmeniKlub(p);
        JOptionPane.showMessageDialog(this, "Uspesno ste izmenili klub", "Izmena uspesna", JOptionPane.INFORMATION_MESSAGE);
        this.dispose();
    }//GEN-LAST:event_btnAzurirajActionPerformed

    public void popuniComboMesta() {
        List<Mesto> mesta = Kontroler.getInstance().vratiMesta();
        for (Mesto m : mesta) {
            comboMesto.addItem(m);
        }
    }

